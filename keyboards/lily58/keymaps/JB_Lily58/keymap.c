static void print_status_narrow(void) {
    /* Print current mode */

   oled_set_cursor(0, 0);

    /* Print current layer */
    oled_write_P(PSTR("Layer:\n "), false);

    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_P(PSTR("Default\n"), false);
            break;
        case _LOWER:
            oled_write_P(PSTR("LOWER\n"), false);
            break;
        case   _RAISE:
            oled_write_P(PSTR("RAISE\n"), false);
            break;
       default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write_ln_P(PSTR("Undefined"), false);
    }
    
    /* Print amount of keys typed */
    char buffer[16]; // small buffer is enough for numbers
    snprintf(buffer, sizeof(buffer), "Keys: %u", amountWordsTyped);
    oled_write(buffer, false);

    /* caps lock */
    oled_set_cursor(0, 3);
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.num_lock ? PSTR("NUM ") : PSTR("    "), false);
    oled_write_P(led_state.caps_lock ? PSTR("CAP ") : PSTR("    "), false);


    /*Dynamic macros */

    oled_set_cursor(0, 5);
    if (cached_direction == 0) {
        oled_write_P(PSTR("    "), false);
    } else if (is_recording_1){
        oled_write_P(PSTR("REC1"), false);
    } else if (!is_recording_1 && cached_direction != 0){
        oled_write_P(PSTR("PLY1"), false);
    }

    oled_set_cursor(5, 5);
    if (cached_direction == 0) {
        oled_write_P(PSTR("    "), false);
    } else if (is_recording_2){
        oled_write_P(PSTR("REC2"), false);
    } else if (!is_recording_2 && cached_direction != 0){
        oled_write_P(PSTR("PLY2"), false);
    }


    /*jiggler*/

    oled_set_cursor(0, 7);
    switch (is_jiggling) {
        case true:
            oled_write_P(PSTR("Jiggle"), false);
            break;
        default:
            oled_write_P(PSTR("      "), false);
   }

    /* KEYBOARD PET RENDER START */
    //x and y (Pos num is down)
    //Y =0 is top of screen
    render_digimon(0,13);
    /* KEYBOARD PET RENDER END */
}

